package week1.day2;

public class LearnOperators {

	public static void main(String[] args) {
		
		int a=5;
		int b=7;
		System.out.println(a+b);
		
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(b/a);
	System.out.println(b%a);
	System.out.println((a*b)+" "+(a%b));
	System.out.println(a==b);
	System.out.println(a<b);
	System.out.println(a>b);
	System.out.println(a<=b);
	
	System.out.println("-----------------");
	
	System.out.println(a++);
	System.out.println(++a);
	System.out.println(a--);
	System.out.println(--a);
	}
}
